package model;

public class Item {

    private Long id;
    private String value;

    public Item(Long id, String value) {
        this.id = id;
        this.value = value;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return value;
    }

}
