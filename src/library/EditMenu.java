    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        Menu menu = new Menu();
        if (menuName.getText().isEmpty() || priceTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun!!!");
        } else {
            try {
                menu.setMenuName(menuName.getText());
                menu.setPrice(Float.valueOf(priceTxt.getText()));

                boolean isUpdated = dao.updateMenu(menu, selectedRow);
                if (isUpdated) {
                    JOptionPane.showMessageDialog(null, "Düzəliş edildi...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Düzəliş edilmədi!!!");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked

