    EditBook(DaoImpl dao, Long selectedRow) {
        try {
            initComponents();
            this.dao = dao;
            this.selectedRow = selectedRow;

            Book book = dao.getBookById(selectedRow);
            bookNameTxt.setText(book.getTitle());
            pageNumTxt.setText(book.getPageNum().toString());
            copiesTxt.setText(String.valueOf(book.getCopies()));
            priceTxt.setText(book.getPrice().toString());

            DefaultComboBoxModel model = (DefaultComboBoxModel) bookTipCombo.getModel();
            List<BookTip> bookTipList = dao.getBookTipList();
            for (BookTip bookTip : bookTipList) {
                model.addElement(new Item(bookTip.getId(), bookTip.getTipName()));
            }
            model.setSelectedItem(new Item(book.getBookTip().getId(), book.getBookTip().getTipName()));

            DefaultComboBoxModel model1 = (DefaultComboBoxModel) shelfCombo.getModel();
            List<Shelf> shelfList = dao.getShelfList();
            for (Shelf shelf : shelfList) {
                model1.addElement(new Item(shelf.getId(), shelf.getShelfName()));
            }
            model1.setSelectedItem(new Item(book.getShelf().getId(), book.getShelf().getShelfName()));

            List<BookStatus> bookStatus = dao.getBookStatus();
            DefaultComboBoxModel model2 = (DefaultComboBoxModel) bookStatusCombo.getModel();
            for (BookStatus status : bookStatus) {
                model2.addElement(new Item(status.getId(), status.getStatusName()));
            }
            model2.setSelectedItem(new Item(book.getBookStatus().getId(), book.getBookStatus().getStatusName()));

            byte[] imgByte = dao.getBookPhotoById(selectedRow);
            if (imgByte != null) {
                ImageIcon image = new ImageIcon(imgByte);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(myImg);
                photo.setIcon(imageIcon);
            } else {
                photo.setIcon(new ImageIcon("nophoto.png"));
                String nophoto = "C:\\Users\\Windows10\\Desktop\\JAVA\\DESKTOP\\DESKTOP_IMG\\nophoto.png";
                fileName = nophoto;
            }
        } catch (Exception ex) {
            Logger.getLogger(EditBook.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

