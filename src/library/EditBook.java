    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        Book book = new Book();
        if (bookNameTxt.getText().isEmpty() || copiesTxt.getText().isEmpty() || pageNumTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun!!!");
        } else {
            try {
                Item shelfItem = (Item) shelfCombo.getSelectedItem();
                Item bookTipItem = (Item) bookTipCombo.getSelectedItem();
                Item bookStatusItem = (Item) bookStatusCombo.getSelectedItem();

                Shelf shelf = new Shelf();
                shelf.setId(shelfItem.getId());

                BookTip bookTip = new BookTip();
                bookTip.setId(bookTipItem.getId());

                BookStatus status = new BookStatus();
                status.setId(bookStatusItem.getId());

                book.setTitle(bookNameTxt.getText());
                book.setPageNum(Long.parseLong(pageNumTxt.getText()));
                book.setCopies(Integer.valueOf(copiesTxt.getText()));
                book.setPrice(Float.valueOf(priceTxt.getText()));
                book.setBookTip(bookTip);
                book.setShelf(shelf);
                book.setBookStatus(status);

                boolean isUpdated = dao.updateBook(book, selectedRow);
                if (fileName != null) {
                    boolean isUpdateBookImg = dao.updateBookImg(selectedRow, fileName);
                }
                if (isUpdated) {
                    JOptionPane.showMessageDialog(null, "Düzəliş edildi...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Düzəliş edilmədi!!!");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_editBtnMouseClicked

