    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        Book book = new Book();
        if (bookNameTxt.getText().isEmpty() || copiesTxt.getText().isEmpty() || pageNumTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun!!!");
        } else {
            try {
                Item shelfItem = (Item) shelfCombo.getSelectedItem();
                Item bookTipItem = (Item) bookTipCombo.getSelectedItem();

                Shelf shelf = new Shelf();
                shelf.setId(shelfItem.getId());

                BookTip bookTip = new BookTip();
                bookTip.setId(bookTipItem.getId());

                book.setTitle(bookNameTxt.getText());
                book.setPageNum(Long.parseLong(pageNumTxt.getText()));
                book.setCopies(Integer.valueOf(copiesTxt.getText()));
                book.setPrice(Float.valueOf(priceTxt.getText()));
                book.setBookTip(bookTip);
                book.setShelf(shelf);

                boolean isAdded = dao.add(book, fileName);
                if (isAdded) {
                    JOptionPane.showMessageDialog(null, "Kitab Əlavə Olundu...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Kitab Əlavə Olunnadı!!!");
                }
            } catch (Exception ex) {
                Logger.getLogger(AddBookFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_saveBtnMouseClicked


