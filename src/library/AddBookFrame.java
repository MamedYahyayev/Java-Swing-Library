    private void ViewShelfListInCombo(List<Shelf> shelfList) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) shelfCombo.getModel();
        for (Shelf shelf : shelfList) {
            model.addElement(new Item(shelf.getId(), shelf.getShelfName()));
        }
    }

    private void ViewBookTipListInCombo(List<BookTip> bookTipList) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) bookTipCombo.getModel();
        for (BookTip bookTip : bookTipList) {
            model.addElement(new Item(bookTip.getId(), bookTip.getTipName()));
        }
    }
