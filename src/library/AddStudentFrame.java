    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        try {
            Student student = new Student();
            if (nameTxt.getText().isEmpty() || surnameTxt.getText().isEmpty() || emailTxt.getText().isEmpty() || groupTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun");
            } else {
                student.setName(nameTxt.getText());
                student.setSurname(surnameTxt.getText());
                student.setGroup(groupTxt.getText());
                student.setEmail(emailTxt.getText());
                student.setPhone(phoneTxt.getText());
                
                boolean isAdded = dao.add(student,fileName);
                if (isAdded) {
                    JOptionPane.showMessageDialog(null, "Tələbə Əlavə Olundu...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Tələbə Əlavə Olunmadı!!!");
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(AddStudentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveBtnMouseClicked

    

