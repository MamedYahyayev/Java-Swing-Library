    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        Author author = Author.getInstance();
        if (nameTxt.getText().isEmpty() || surnameTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun!!!");
        } else {
            try {
                author.setName(nameTxt.getText());
                author.setSurname(surnameTxt.getText());
                author.setBirthDate(birthDate.getDate());
                author.setDeathDate(deathDate.getDate());
                System.out.println(fileName);
                boolean isUpdated = dao.updateAuthor(author, selectedRow);
                if (fileName != null) {
                    boolean isUpdateImg = dao.updateAuthorImg(selectedRow, fileName);
                }
                if (isUpdated) {
                    JOptionPane.showMessageDialog(null, "Düzəliş edildi...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Düzəliş edilmədi!!!");
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked


