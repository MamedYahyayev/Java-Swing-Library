    private void yesBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_yesBtnMouseClicked
        try {
            if ("sold".equals(globCheckName)) {
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 1);
                Book book = dao.getBookById(orderBook.getBook().getId());
                int copies = book.getCopies() + 1;
                boolean plusBookNum = dao.updateBookNum(copies, book.getId());
                boolean isDelete = dao.deleteOrderBook(selectedRow);
                if (isDelete) {
                    JOptionPane.showMessageDialog(null, "Qaytarildi...");
                    this.dispose();
                }
            }
            if ("borrow".equals(globCheckName)) {
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 2);
                Book book = dao.getBookById(orderBook.getBook().getId());
                int copies = book.getCopies() + 1;
                boolean plusBookNum = dao.updateBookNum(copies, book.getId());
                boolean isDelete = dao.deleteOrderBook(selectedRow);
                if (isDelete) {
                    JOptionPane.showMessageDialog(null, "Qaytarildi...");
                    this.dispose();
                }
            }
            if ("readNow".equals(globCheckName)) {
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 4);
                Book book = dao.getBookById(orderBook.getBook().getId());
                int copies = book.getCopies() + 1;
                boolean plusBookNum = dao.updateBookNum(copies, book.getId());
                boolean isDelete = dao.deleteOrderBook(selectedRow);
                if (isDelete) {
                    JOptionPane.showMessageDialog(null, "Qaytarildi...");
                    this.dispose();
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(GiveBackFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_yesBtnMouseClicked

