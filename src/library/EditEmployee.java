    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        if (nameTxt.getText().isEmpty() || surnameTxt.getText().isEmpty() || addressTxt.getText().isEmpty()
                || workDayTxt.getText().isEmpty() || phoneTxt.getText().isEmpty()
                || salaryTxt.getText().isEmpty() || emailTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bütün Xanaları Doldurun !!!");
        } else {

            try {
                Employee employee = new Employee();
                employee.setName(nameTxt.getText());
                employee.setSurname(surnameTxt.getText());
                employee.setAddress(addressTxt.getText());
                employee.setSalary(Float.valueOf(salaryTxt.getText()));
                employee.setWorkDay(workDayTxt.getText());
                employee.setPhone(phoneTxt.getText());
                employee.setEmail(emailTxt.getText());
                
                boolean isUpdated = dao.updateEmployee(employee, selectedRow);
                if (fileName != null) {
                    boolean isUpdateImg = dao.updateEmployeeImg(selectedRow, fileName);
                }
                if (isUpdated) {
                    JOptionPane.showMessageDialog(null, "Düzəliş edildi...");
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Düzəliş edilmədi!!!");
                }
            } catch (Exception ex) {
                Logger.getLogger(EditEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_editBtnMouseClicked

