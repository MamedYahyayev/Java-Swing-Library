    ViewEmployee(DaoImpl dao, Long selectedRow) {
        try {
            initComponents();
            this.dao = dao;
            this.selectedRow = selectedRow;

            Employee employee = dao.getEmployeeById(selectedRow);
            nameTxt.setText(employee.getName());
            surnameTxt.setText(employee.getSurname());
            phoneTxt.setText(employee.getPhone());
            emailTxt.setText(employee.getEmail());
            addressTxt.setText(employee.getAddress());
            workDayTxt.setText(employee.getWorkDay());
            salaryTxt.setText(employee.getSalary() + " AZN");
            
            
            byte[] imgByte = dao.getEmployeePhotoById(selectedRow);
            if (imgByte != null) {
                ImageIcon image = new ImageIcon(imgByte);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(myImg);
                photo.setIcon(imageIcon);
            } else {
                photo.setIcon(new ImageIcon("nophoto.png"));
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

