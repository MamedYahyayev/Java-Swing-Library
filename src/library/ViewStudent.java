    ViewStudent(DaoImpl dao, Long selectedRow) {
        try {
            initComponents();
            this.dao = dao;
            this.selectedRow = selectedRow;

            Student student = dao.getStudentById(selectedRow);
            nameTxt.setText(student.getName());
            surnameTxt.setText(student.getSurname());
            emailTxt.setText(student.getEmail());
            groupTxt.setText(student.getGroup());
            phoneTxt.setText(student.getPhone());

            byte[] imgByte = dao.getStudentPhotoById(selectedRow);
            if (imgByte != null) {
                ImageIcon image = new ImageIcon(imgByte);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(myImg);
                photo.setIcon(imageIcon);
            } else {
                photo.setIcon(new ImageIcon("nophoto.png"));
            }

        } catch (Exception ex) {
            Logger.getLogger(ViewStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

