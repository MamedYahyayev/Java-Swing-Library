    private void paidBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_paidBtnMouseClicked
        try {
            TableOrder tableOrder = new TableOrder();
            tableOrder.setId(selectedRow);

            Paid paid = new Paid();
            paid.setTableOrder(tableOrder);
            paid.setPrice(price);

            boolean isAdded = dao.add(paid);
            if (isAdded) {
                TableOrder tableOrderForTable = dao.getTableOrderById(selectedRow);
                boolean isDelete = dao.deleteTableOrderByTableId(tableOrderForTable.getTable().getId());
                if (isDelete) {
                    JOptionPane.showMessageDialog(null, "Ödənildi...");
                    boolean changeTableStatus = dao.changeTableStatus(tableOrderForTable.getTable().getId(), (long) 1);
                    this.dispose();
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(PaidFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_paidBtnMouseClicked


