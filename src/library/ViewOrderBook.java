    ViewOrderBook(DaoImpl dao, Long selectedRow, String globCheckName) {
        initComponents();
        this.dao = dao;
        this.selectedRow = selectedRow;
        this.globCheckName = globCheckName;
        
        if ("sold".equals(globCheckName)) {
            try {
                jLabel6.setVisible(false);
                borrowTime.setVisible(false);
                
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 1);
                nameTxt.setText(orderBook.getStudent().getName());
                surnameTxt.setText(orderBook.getStudent().getSurname());
                phoneTxt.setText(orderBook.getStudent().getPhone());
                bookTitleTxt.setText(orderBook.getBook().getTitle());
                priceTxt.setText(orderBook.getBook().getPrice().toString());
            } catch (Exception ex) {
                Logger.getLogger(ViewOrderBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        if ("borrow".equals(globCheckName)) {
            try {
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 2);
                nameTxt.setText(orderBook.getStudent().getName());
                surnameTxt.setText(orderBook.getStudent().getSurname());
                phoneTxt.setText(orderBook.getStudent().getPhone());
                bookTitleTxt.setText(orderBook.getBook().getTitle());
                priceTxt.setText(orderBook.getBook().getPrice().toString());
                borrowTime.setText(orderBook.getBorrowTime().toString());
            } catch (Exception ex) {
                Logger.getLogger(ViewOrderBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if ("readNow".equals(globCheckName)) {
            try {
                jLabel5.setVisible(false);
                jLabel6.setVisible(false);
                priceTxt.setVisible(false);
                borrowTime.setVisible(false);
                OrderBook orderBook = dao.getOrderBookById(selectedRow, (long) 4);
                nameTxt.setText(orderBook.getStudent().getName());
                surnameTxt.setText(orderBook.getStudent().getSurname());
                phoneTxt.setText(orderBook.getStudent().getPhone());
                bookTitleTxt.setText(orderBook.getBook().getTitle());
            } catch (Exception ex) {
                Logger.getLogger(ViewOrderBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

