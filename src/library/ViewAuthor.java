    ViewAuthor(DaoImpl dao, Long selectedRow) {
        try {
            initComponents();
            this.dao = dao;
            this.selectedRow = selectedRow;
            

           
                    
            Author author = dao.getAuthorById(selectedRow);
            nameTxt.setText(author.getName());
            surnameTxt.setText(author.getSurname());
            birthDate.setDate(author.getBirthDate());
            deathDate.setDate(author.getDeathDate());

            byte[] imgByte = dao.getAuthorPhotoById(selectedRow);
            if (imgByte != null) {
                ImageIcon image = new ImageIcon(imgByte);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(photo.getWidth(), photo.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(myImg);
                photo.setIcon(imageIcon);
            } else {
                photo.setIcon(new ImageIcon("nophoto.png"));
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewAuthor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

